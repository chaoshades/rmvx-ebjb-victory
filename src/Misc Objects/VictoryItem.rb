#==============================================================================
# ** VictoryItem
#------------------------------------------------------------------------------
#  Custom object used to represent an item won after battle
#==============================================================================

class VictoryItem
  
  #//////////////////////////////////////////////////////////////////////////
  # * Attributes
  #//////////////////////////////////////////////////////////////////////////
  
  # Item object
  attr_reader :item
  # Quantity of the item obtained
  attr_accessor :quantity
  
  #//////////////////////////////////////////////////////////////////////////
  # * Constructors
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Object Initialization
  #     item : item object
  #     quantity : quantity of the item obtained
  #--------------------------------------------------------------------------
  def initialize(item, quantity)
    @item = item
    @quantity = quantity
  end
  
end
